/************************************************************************************/
void set_pin(uint8_t pin)
{

  digitalWrite(pin, HIGH);
}

/************************************************************************************/
void clear_pin(uint8_t pin)
{

  digitalWrite(pin, LOW);
}

/************************************************************************************/
bool read_digital_pin(int pin)
{
  return bitRead(pin, OUTPUT);
}
